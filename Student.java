public class Student {
    int id;
    String name;
    String address;
    String dateOfBirth;
    int age;
    int sum;
    boolean isDigit;

    public Student(int id, String name, String address, String dateOfBirth) {
        this.id = id;
        this.name = name;
        this.address = address;
        this.dateOfBirth = dateOfBirth;
    }
}

